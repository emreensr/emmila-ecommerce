<script setup>

const { closeCart, isOpen } = defineProps(['closeCart', 'isOpen']);
const handleClickOutside = (event) => {
    const whiteSide = document.querySelector(".whiteSide");
    const blackSide = document.querySelector(".blackSide");
    const closeCartIcon = document.querySelector(".closeCartIcon");
    try {
        if ((closeCartIcon.contains(event?.target))) {
            closeCart()
        }
        else if (
            (!whiteSide.contains(event?.target)) && (blackSide.contains(event?.target))
        ) {
            closeCart()
        }
    } catch (error) {
        console.log(error);
    }

};
onMounted(async () => {
    await nextTick();
    document.addEventListener("click", handleClickOutside);
});

onBeforeUnmount(() => {
    document.removeEventListener("click", handleClickOutside);
});

